algo = input("digite alguma coisa: ")
print("o tipo primitivo é {}".format(type(algo)))
print('esta escrito tudo em maiúscilo? {}'.format(algo.isupper()))
print('esta escrito tudo em minúscilo? {}'.format(algo.islower()))
print('é um número? {}'.format(algo.isnumeric()))
print('são letras do alfabeto? {}'.format(algo.isalpha()))

